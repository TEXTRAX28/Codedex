num = [1,2,3,4,5,6,7,8,9]

# print num
print(num)

# print maximum number
max_num = max(num)
print(max_num)

# print minimum number
min_num = min(num)
print(min_num)

# print the length of the num
print(len(num))

# power
print(pow(2,6))

# enumerate
for i, value in enumerate(num):
  print(f"{i}. {value}")
